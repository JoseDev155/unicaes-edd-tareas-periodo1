// Array
let tareas = ["Desayunar", "Estudiar", "Hacer ejercicio", "Leer", "Dormir"];

// 1 - Elimina la última tarea usando pop() y muestra cuál fue la tarea eliminada
let tareaEliminada = tareas.pop();

// 2 - Elimina nuevamente el último elemento y muestra el estado actualizado del array
let tareaEliminada2 = tareas.pop();

console.log("Tarea eliminada:", tareaEliminada);
console.log("Tarea eliminada nuevamente:", tareaEliminada2);
console.log("Estado actualizado del array de tareas:", tareas);