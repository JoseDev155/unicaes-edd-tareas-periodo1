// Crea un array con 4 nombres de productos
let productos = ["Manzana", "Banana", "Naranja", "Pera"];

// Agrega 2 nuevos productos al final usando .push()
productos.push("Uva", "Kiwi");
console.log("Lista completa de productos:", productos);
// Elimina el último producto usando .pop()
productos.pop();
// Muestra el array después de cada operación
console.log("Lista después de eliminar el último producto:", productos);